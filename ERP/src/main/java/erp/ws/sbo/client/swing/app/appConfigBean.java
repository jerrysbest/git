package erp.ws.sbo.client.swing.app;

public class appConfigBean {
	private String lserver;   
	private String dserver;
	private String comdb;
	private String dbusername;
	private String dbuserpas;
	 public String getLserver() {   
	  return lserver;   
	 }   	  
	 public void setLserver(String lserver) {   
	  this.lserver = lserver;   
	 }  	 
	 public String getDserver() {   
	  return dserver;   
	 }   	  
	 public void setDserver(String dserver) {   
	  this.dserver = dserver;   
	 }
	public String getComdb() {
		return comdb;
	}
	public void setComdb(String comdb) {
		this.comdb = comdb;
	}
	public String getDbusername() {
		return dbusername;
	}
	public void setDbusername(String dbusername) {
		this.dbusername = dbusername;
	}
	public String getDbuserpas() {
		return dbuserpas;
	}
	public void setDbuserpas(String dbuserpas) {
		this.dbuserpas = dbuserpas;
	}  
}
